
public class Position {
	private double x, y;
	
	Position(double x, double y){
		this.x = x;
		this.y = y;
	}
	
	public void setXPos(double x) {
		this.x = x;
	}
	public double getXPos() {
		return x;
	}
	public void setYPos(double y) {
		this.y = y;
	}
	public double getYPos() {
		return y;
	}
}
